package LOOPS;

public class _DO_WHILE_LOOP {

	public static void main(String[] args) {
		//Print 1 to 100 numbers using do while loop
		//Syntax:
		//initialization
		  //do{
		      //increment/decrement operator
		   //}while(condition);
		
		int i = 200;
		do {
			System.out.println(i);
			i++;
		}while(i<=100);
	}
}
