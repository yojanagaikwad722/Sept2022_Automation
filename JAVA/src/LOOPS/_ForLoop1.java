package LOOPS;

public class _ForLoop1 {

	public static void main(String[] args) {
		//print numbers from 1 to 10
		//start no. = 1
		//end no. = 10
		//increment
		//condition : relation between start no and end no. <=
		
		//Syntax:
		//for(initialization; condition; increment/decrement operator){
		//
		//}
		
		for(int i=1; i<=10; i++) {//2 3 4.........9 10 11
			System.out.println(i);//1 2 3 4.....9 10
		}
	}
}
