package LOOPS;

public class _WHILE_LOOP {

	public static void main(String[] args) {
		//print numbers from 1 to 10

		//Syntax:
		 //initialization
		   // while(condition){
		       //increment/decrement operator
		  //}
		
		int i = 1;
		while(i<=10) {
			System.out.println(i);
			i++;
		}
	}
}
